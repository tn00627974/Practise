
// 數字常見的用法 (整數 浮點數 )

// 除法
System.Console.WriteLine(5 / 2);

// 浮點數除法
System.Console.WriteLine(5.0 / 2);


// Math.Abs(-10)
System.Console.WriteLine(System.Math.Abs(-10)); // 取得10

// Math.Pow 次方(4,3) = 4的3次方
System.Console.WriteLine(System.Math.Pow(4,3)); // 64

// Math.Sqrt 開根號
System.Console.WriteLine(System.Math.Sqrt(64)); // 8

// Math.Max
System.Console.WriteLine(System.Math.Max(10.0,3)); // 10

// Math.Min
System.Console.WriteLine(System.Math.Min(10.0,3)); // 3

// Math.Round (3.6) = 四捨五入 3.6
System.Console.WriteLine(System.Math.Round(10.0, 3)); // 4


